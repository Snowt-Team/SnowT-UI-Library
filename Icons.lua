-- Cache for loaded images
local iconCache = {}
local defaultIcon = "http://www.roblox.com/asset/?id=6031327880" -- Fallback icon

-- Initialize module with SnowtUI context
function Icons.init(params)
    local SnowtUI = params.SnowtUIInstance
    Icons.SnowtUI = SnowtUI
    Icons.Theme = SnowtUI:GetModule("Theme")
    Icons.Config = SnowtUI:GetModule("Config")
    
    -- Preload critical icons
    Icons:preload("hourglass_top") -- For LoadingScreen.lua
end

-- Get icon URL by name and set
function Icons.get_icon(set, name)
    if not set or not name then
        warn("Icons.get_icon: Missing set or name")
        return defaultIcon
    end

    local iconSet = Icons.IconSets[set]
    if not iconSet then
        warn(string.format("Icons.get_icon: Invalid set '%s'", set))
        return defaultIcon
    end

    local iconUrl = iconSet[name]
    if not iconUrl then
        warn(string.format("Icons.get_icon: Icon '%s' not found in set '%s'", name, set))
        return defaultIcon
    end

    return iconUrl
end

-- Preload icon and cache it
function Icons.preload(iconName, set)
    set = set or "Material"
    local iconUrl = Icons.get_icon(set, iconName)
    if iconCache[iconUrl] then
        return iconCache[iconUrl]
    end

    local success, result = pcall(function()
        ContentProvider:PreloadAsync({iconUrl})
        return iconUrl
    end)

    if success then
        iconCache[iconUrl] = iconUrl
        return iconUrl
    else
        warn(string.format("Icons.preload: Failed to preload icon '%s' in set '%s': %s", iconName, set, result))
        return defaultIcon
    end
end

-- Apply themed icon to UI element
function Icons.apply_to(element, iconName, set, properties)
    set = set or "Material"
    properties = properties or {}
    local iconUrl = Icons.preload(iconName, set)
    
    if element:IsA("ImageLabel") or element:IsA("ImageButton") then
        element.Image = iconUrl
        element.ImageColor3 = Icons.Theme.get_color("IconColor", properties.theme or "Default")
        
        -- Apply additional properties
        for key, value in pairs(properties) do
            if key ~= "theme" then
                element[key] = value
            end
        end
    else
        warn(string.format("Icons.apply_to: Element must be ImageLabel or ImageButton, got %s", element.ClassName))
    end
end

-- Clear icon cache
function Icons.clear_cache()
    iconCache = {}
end

-- Register icon set (for dynamic additions)
function Icons.register_set(setName, icons)
    if Icons.IconSets[setName] then
        warn(string.format("Icons.register_set: Set '%s' already exists", setName))
        return
    end
    Icons.IconSets[setName] = icons
end

-- Get all available icon names in a set
function Icons.get_icon_names(set)
    set = set or "Material"
    local iconSet = Icons.IconSets[set]
    if not iconSet then
        warn(string.format("Icons.get_icon_names: Invalid set '%s'", set))
        return {}
    end
    
    local names = {}
    for name, _ in pairs(iconSet) do
        table.insert(names, name)
    end
    table.sort(names)
    return names
end

-- Handle theme updates
function Icons.on_theme_update()
    for _, element in pairs(iconCache) do
        if typeof(element) == "Instance" and (element:IsA("ImageLabel") or element:IsA("ImageButton")) then
            element.ImageColor3 = Icons.Theme.get_color("IconColor")
        end
    end
end

-- Connect to SnowtUI lifecycle
function Icons.connect_lifecycle()
    if Icons.SnowtUI then
        Icons.SnowtUI:OnModuleLoaded("Theme", function(theme)
            Icons.Theme = theme
            theme:Subscribe("ThemeUpdated", Icons.on_theme_update)
        end)
        
        -- Register loading screen icon
        Icons.SnowtUI:OnModuleLoaded("LoadingScreen", function(loadingScreen)
            loadingScreen:SetSpinnerIcon(Icons.get_icon("Material", "hourglass_top"))
        end)
    end
end

-- Initialize lifecycle connections
Icons.connect_lifecycle()

return Icons